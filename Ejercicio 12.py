'''Ejercicio 12: Calculadora de Área de un Rectángulo
Crea un programa que calcule el área de un rectángulo. El usuario debe ingresar la
longitud y el ancho del rectángulo'''

# Programa para calcular el área de un rectángulo

# Solicitar al usuario la longitud y el ancho
longitud = float(input("Ingrese la longitud del rectángulo: "))
ancho = float(input("Ingrese el ancho del rectángulo: "))

# Calcular el área
area = longitud * ancho

# Mostrar el resultado
print(f"El área del rectángulo es: {area}")